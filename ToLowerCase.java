package Day2;

public class ToLowerCase {

	public static void main(String[] args) {

		String s= "AVINASH";
		System.out.println(s.toLowerCase()); // String.tolowercase converts capital letter into small letter
		
		String s1 = "avinash";
		System.out.println(s1.toUpperCase());
		
		String s3 = "Yadav";
		System.out.println(s3.toUpperCase());
		
	}

}
