package da3;

public class EnhancedForLoop {

	public static void main(String[] args) {

		int a [] = {2,3,4,5,6,7,8,9};
		
		for(int c: a) {  // yaha c ko variable ke taur par le rahe hai
			
			System.out.println(c); 
		}
		
		int b[] = {3,5,6,6};
		for(int v1 : b) {
			System.out.println("\n"+v1);
		}
		
		
		
		
		
		
	}

}
