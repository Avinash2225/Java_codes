package FrequentlyAskedJavaInterviewQuestions;

public class SwapTwoNumbers {

	public static void main(String[] args) {

		int x = 5;
		int y = 10;
		
	x= x*y/x;
	x= 10;
	y= x*y/y;
	y=5;
		
		
	
		System.out.println("the value value of the x after swapping:"+ x);		
		System.out.println("the value value of the x after swapping:"+y);		 
	
	}

}
